let a = 20;
let b = 10;
let operator = '*';

switch (operator) {
    case '+': console.log("Sum:", a + b); break;
    case '-': console.log("Difference:", a - b); break;
    case '*': console.log("Product:", a * b); break;
    case '/': console.log("Quotient:", a / b); break;
    default: console.log("Invalid operator");
}
